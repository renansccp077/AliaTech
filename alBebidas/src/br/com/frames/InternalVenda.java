    public void carregaTabelaLancados(){
        DefaultTableModel modelo = (DefaultTableModel) mostraProdutos3.getModel();
        modelo.setNumRows(0);
        VendaDAO dao = new VendaDAO();
        List<Venda> vendas = new ArrayList<>();
        vendas = venda;
        for(Venda v: vendas){
            
            modelo.addRow(new Object[]{       
                v.getNomeproduto(),
                v.getPrecoUnitario(),
                v.getQuantidade(),
                v.getValorTotal()    
            });
        }
    }

